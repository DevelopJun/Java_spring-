package ch07;

public class DoubleTest {

	public static void main(String[] args) {

		double dnum = 3.14; // 모든 리터럴들은 double 로 되어 있고 밑에 내려가면 이제 f를 지정해줘야함.
		float fnum = 3.14f; //f 적어야 함. float 식별자 써야한다. 일반적으로 double 많이 쓴다.
		
		System.out.println(dnum);
		System.out.println(fnum); //실수가 어떻게 모든 숫자를 double 형으로 저장되어 있다. 메모리에. 그래서 항상 float를 적어야 한다 . 만약에 바꾸고 싶으면, 
		
		
	}

}
